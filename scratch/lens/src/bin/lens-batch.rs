//! Slice 1.6 — batch driver across N SWE-bench instances.
//!
//! Discovers PASS instances under `scratch/latent/out/inst-*/f2p.json`,
//! subprocesses the `lens` binary per instance with the right fixture paths,
//! and aggregates per-instance JSONLs into one batch JSONL + a summary.json.
//!
//! Run:
//! ```sh
//! cargo run --release --features real --bin lens-batch -- \
//!     --fixture-root /home/ivy/Code/glia/scratch/latent/out \
//!     --weights /home/ivy/Models/qwen2.5-coder-7b-gguf/qwen2.5-coder-7b-instruct-q4_k_m.gguf \
//!     --tokenizer /home/ivy/Models/qwen2.5-coder-tokenizer/tokenizer.json \
//!     --max-instances 20 \
//!     --output-positions 0..16 \
//!     --out-dir /home/ivy/Code/glia/scratch/lens/out/batch
//! ```
//!
//! Output:
//! - `<out-dir>/<instance>.jsonl` per instance (raw lens output)
//! - `<out-dir>/<instance>.png` per instance (heatmap)
//! - `<out-dir>/<instance>.log` per instance (stderr from lens subprocess)
//! - `<out-dir>/all-instances.jsonl` aggregated, with `instance_id` injected
//! - `<out-dir>/summary.json` per-instance: token counts, PASS/FAIL, runtime

use std::collections::BTreeMap;
use std::path::{Path, PathBuf};
use std::process::{Command, Stdio};
use std::time::{Instant, SystemTime, UNIX_EPOCH};

use anyhow::{Context, Result};
use clap::Parser;
use serde::{Deserialize, Serialize};
use walkdir::WalkDir;

#[derive(Parser, Debug)]
#[command(name = "lens-batch", version)]
struct Args {
    /// Root containing `inst-*/f2p.json` and `<instance>-pathB.{prefix,suffix}`
    /// + `<instance>.summaries.json`.
    #[arg(long, default_value = "/home/ivy/Code/glia/scratch/latent/out")]
    fixture_root: PathBuf,

    /// Path to the GGUF weights — passed through to `lens`.
    #[arg(long)]
    weights: PathBuf,

    /// Path to the tokenizer.json — passed through to `lens`.
    #[arg(long)]
    tokenizer: PathBuf,

    /// Cap on number of instances to run.
    #[arg(long, default_value_t = 20)]
    max_instances: usize,

    /// Positions spec forwarded to `lens --output-positions`.
    #[arg(long, default_value = "0..16")]
    output_positions: String,

    /// Top-K forwarded to `lens`.
    #[arg(long, default_value_t = 5)]
    top_k: usize,

    /// Max-injection-entries forwarded to `lens`. Caps the serialized
    /// injection to the top-N by score so the prompt fits ctx.
    #[arg(long, default_value_t = 30)]
    max_injection_entries: usize,

    /// Output directory for per-instance JSONL/PNG/log + aggregated artefacts.
    #[arg(long)]
    out_dir: PathBuf,

    /// Path to the `lens` binary. Defaults to the workspace-relative path.
    #[arg(long, default_value = "/home/ivy/Code/glia/scratch/lens/target/release/lens")]
    lens_bin: PathBuf,

    /// Only consider instances whose project name matches this substring
    /// (e.g. "django" to scope to one project).
    #[arg(long)]
    project_filter: Option<String>,

    /// Include FAIL instances too (default: PASS only). The analyzer needs
    /// both classes for stratified pattern detection.
    #[arg(long, default_value_t = true)]
    include_fail: bool,

    /// Skip instances whose <out>/<instance>.jsonl already exists with
    /// non-zero size (lets you resume an interrupted batch).
    #[arg(long, default_value_t = true)]
    skip_existing: bool,

    /// Lens mode forwarded to each subprocess (D1, cycle 0.6). "generate"
    /// runs autoregressive decode + captures per-generated-token residuals
    /// (real-pipeline-comparable). "prompt-position" stays with the slice 1.5
    /// per-prompt-position scan.
    #[arg(long, value_parser = clap::builder::PossibleValuesParser::new(["prompt-position", "generate"]), default_value = "prompt-position")]
    mode: String,

    /// In generate mode, forwarded as --max-new. Ignored in prompt-position
    /// mode.
    #[arg(long, default_value_t = 64)]
    max_new: u32,

    /// Directory containing per-instance graph-derived directive files named
    /// `<instance_id>.md`. When present + non-empty for an instance, forwarded
    /// to lens as `--directive`. Generated by `synth_directive` (B4). Lets
    /// cycle 0.7+ lens runs mirror real-pipeline suffix injection.
    #[arg(long)]
    directive_dir: Option<PathBuf>,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
struct InstanceFixture {
    instance_id: String,
    project: String,
    pass: bool,
    run_dir: PathBuf,
    prefix: PathBuf,
    suffix: PathBuf,
    summaries: PathBuf,
    /// Recency timestamp from the f2p.json mtime (Unix seconds).
    recency: u64,
}

#[derive(Debug, Serialize, Deserialize)]
struct InstanceSummary {
    instance_id: String,
    project: String,
    pass: bool,
    f2p_count: u32,
    runtime_secs: f64,
    exit_code: i32,
    jsonl_rows: usize,
    error: Option<String>,
}

#[derive(Debug, Serialize, Deserialize)]
struct BatchSummary {
    fixture_root: PathBuf,
    weights: PathBuf,
    output_positions: String,
    n_attempted: usize,
    n_succeeded: usize,
    n_failed: usize,
    total_runtime_secs: f64,
    instances: Vec<InstanceSummary>,
}

fn main() -> Result<()> {
    tracing_subscriber::fmt()
        .with_env_filter(
            tracing_subscriber::EnvFilter::try_from_default_env()
                .unwrap_or_else(|_| tracing_subscriber::EnvFilter::new("info")),
        )
        .init();

    let args = Args::parse();
    std::fs::create_dir_all(&args.out_dir)?;

    let discovered = discover_instances(&args.fixture_root)?;
    tracing::info!("discovered {} instance fixtures", discovered.len());

    let mut selected: Vec<InstanceFixture> = discovered
        .into_iter()
        .filter(|f| args.include_fail || f.pass)
        .filter(|f| {
            args.project_filter
                .as_ref()
                .is_none_or(|sub| f.project.contains(sub))
        })
        .collect();
    selected.sort_by_key(|f| std::cmp::Reverse(f.recency));
    selected.truncate(args.max_instances);

    let pass_count = selected.iter().filter(|f| f.pass).count();
    let fail_count = selected.len() - pass_count;
    tracing::info!(
        "selected {} instances (pass={} fail={})",
        selected.len(),
        pass_count,
        fail_count,
    );

    let batch_start = Instant::now();
    let mut summary = BatchSummary {
        fixture_root: args.fixture_root.clone(),
        weights: args.weights.clone(),
        output_positions: args.output_positions.clone(),
        n_attempted: 0,
        n_succeeded: 0,
        n_failed: 0,
        total_runtime_secs: 0.0,
        instances: Vec::new(),
    };

    let agg_path = args.out_dir.join("all-instances.jsonl");
    let mut agg = std::io::BufWriter::new(std::fs::File::create(&agg_path)?);
    use std::io::Write;

    for (i, fixture) in selected.iter().enumerate() {
        tracing::info!(
            "[{}/{}] {} (pass={})",
            i + 1,
            selected.len(),
            fixture.instance_id,
            fixture.pass,
        );
        summary.n_attempted += 1;

        let inst_jsonl = args.out_dir.join(format!("{}.jsonl", fixture.instance_id));
        let inst_png = args.out_dir.join(format!("{}.png", fixture.instance_id));
        let inst_log = args.out_dir.join(format!("{}.log", fixture.instance_id));

        if args.skip_existing && inst_jsonl.exists() {
            if let Ok(meta) = std::fs::metadata(&inst_jsonl) {
                if meta.len() > 0 {
                    tracing::info!("  -> skipping (exists)");
                    let rows = count_jsonl_rows(&inst_jsonl).unwrap_or(0);
                    aggregate(&mut agg, &inst_jsonl, &fixture.instance_id)?;
                    summary.instances.push(InstanceSummary {
                        instance_id: fixture.instance_id.clone(),
                        project: fixture.project.clone(),
                        pass: fixture.pass,
                        f2p_count: 0,
                        runtime_secs: 0.0,
                        exit_code: 0,
                        jsonl_rows: rows,
                        error: None,
                    });
                    summary.n_succeeded += 1;
                    continue;
                }
            }
        }

        let inst_start = Instant::now();
        let log_file = std::fs::File::create(&inst_log)?;

        let mut cmd = Command::new(&args.lens_bin);
        cmd.arg("--weights").arg(&args.weights);
        cmd.arg("--tokenizer").arg(&args.tokenizer);
        cmd.arg("--prefix").arg(&fixture.prefix);
        cmd.arg("--suffix").arg(&fixture.suffix);
        cmd.arg("--injection").arg(&fixture.summaries);
        cmd.arg("--output-positions").arg(&args.output_positions);
        cmd.arg("--top-k").arg(args.top_k.to_string());
        cmd.arg("--max-injection-entries").arg(args.max_injection_entries.to_string());
        cmd.arg("--out").arg(&inst_jsonl);
        cmd.arg("--render-png").arg(&inst_png);
        cmd.arg("--label").arg(&fixture.instance_id);
        cmd.arg("--mode").arg(&args.mode);
        if args.mode == "generate" {
            cmd.arg("--max-new").arg(args.max_new.to_string());
        }
        if let Some(dir) = &args.directive_dir {
            let dir_file = dir.join(format!("{}.md", fixture.instance_id));
            if dir_file.exists()
                && std::fs::metadata(&dir_file).map(|m| m.len() > 0).unwrap_or(false)
            {
                cmd.arg("--directive").arg(&dir_file);
            }
        }

        let status = cmd
            .stdout(Stdio::from(
                log_file.try_clone().expect("clone log file for stdout"),
            ))
            .stderr(Stdio::from(log_file))
            .status();

        let runtime_secs = inst_start.elapsed().as_secs_f64();
        let (exit_code, error) = match status {
            Ok(s) => (s.code().unwrap_or(-1), None),
            Err(e) => (-1, Some(format!("spawn failed: {e}"))),
        };

        let (jsonl_rows, agg_err) = if exit_code == 0 && inst_jsonl.exists() {
            let rows = count_jsonl_rows(&inst_jsonl).unwrap_or(0);
            match aggregate(&mut agg, &inst_jsonl, &fixture.instance_id) {
                Ok(_) => (rows, None),
                Err(e) => (rows, Some(format!("aggregate failed: {e}"))),
            }
        } else {
            (0, error.clone().or_else(|| Some(format!("exit code {exit_code}"))))
        };

        let final_error = error.or(agg_err);
        let succeeded = exit_code == 0 && final_error.is_none();
        if succeeded {
            summary.n_succeeded += 1;
        } else {
            summary.n_failed += 1;
            tracing::warn!(
                "  -> failed (exit={exit_code}, err={:?})",
                final_error,
            );
        }

        summary.instances.push(InstanceSummary {
            instance_id: fixture.instance_id.clone(),
            project: fixture.project.clone(),
            pass: fixture.pass,
            f2p_count: count_f2p_passes(&fixture.run_dir).unwrap_or(0),
            runtime_secs,
            exit_code,
            jsonl_rows,
            error: final_error,
        });
    }

    summary.total_runtime_secs = batch_start.elapsed().as_secs_f64();
    agg.flush()?;

    let summary_path = args.out_dir.join("summary.json");
    let summary_text = serde_json::to_string_pretty(&summary)?;
    std::fs::write(&summary_path, summary_text)?;
    tracing::info!(
        "DONE: {}/{} succeeded in {:.1}s — summary at {}",
        summary.n_succeeded,
        summary.n_attempted,
        summary.total_runtime_secs,
        summary_path.display(),
    );

    Ok(())
}

/// Walk `<fixture_root>/inst-*` for f2p.json + cross-reference fixtures.
fn discover_instances(fixture_root: &Path) -> Result<Vec<InstanceFixture>> {
    let mut out = Vec::new();
    for entry in WalkDir::new(fixture_root)
        .min_depth(1)
        .max_depth(1)
        .into_iter()
        .filter_map(|e| e.ok())
    {
        if !entry.file_type().is_dir() {
            continue;
        }
        let dir_name = entry.file_name().to_string_lossy().to_string();
        let Some(instance_with_model) = dir_name.strip_prefix("inst-") else {
            continue;
        };
        // "<org>__<repo>-<id>-<model>-<tag>" → strip the trailing model tags.
        let instance_id = strip_model_tags(instance_with_model);
        let project = instance_id
            .split("__")
            .next()
            .unwrap_or(&instance_id)
            .to_string();

        let f2p_json = entry.path().join("f2p.json");
        let (pass, recency) = match std::fs::metadata(&f2p_json) {
            Ok(meta) => {
                let pass = count_f2p_passes(entry.path()).unwrap_or(0) > 0;
                let recency = meta
                    .modified()
                    .ok()
                    .and_then(|m| m.duration_since(UNIX_EPOCH).ok())
                    .map(|d| d.as_secs())
                    .unwrap_or(0);
                (pass, recency)
            }
            Err(_) => (false, 0),
        };

        // Resolve fixture paths. Two layouts coexist:
        // 1. Per-instance dir layout (the common case): prefix.txt, suffix.txt,
        //    summaries-atomic.json (or summaries-aplus.json) inside the inst-* dir.
        // 2. Marshmallow legacy: <short>-pathB.{prefix,suffix} + <short>.summaries.json
        //    at fixture_root top level.
        let (prefix, suffix, summaries) = if entry.path().join("prefix.txt").exists()
            && entry.path().join("suffix.txt").exists()
        {
            let pre = entry.path().join("prefix.txt");
            let suf = entry.path().join("suffix.txt");
            let summ = if entry.path().join("summaries-atomic.json").exists() {
                entry.path().join("summaries-atomic.json")
            } else if entry.path().join("summaries-aplus.json").exists() {
                entry.path().join("summaries-aplus.json")
            } else {
                continue;
            };
            (pre, suf, summ)
        } else {
            let short_id = short_instance_id(&instance_id);
            let pre = fixture_root.join(format!("{short_id}-pathB.prefix"));
            let suf = fixture_root.join(format!("{short_id}-pathB.suffix"));
            let summ = fixture_root.join(format!("{short_id}.summaries.json"));
            if !pre.exists() || !suf.exists() || !summ.exists() {
                continue;
            }
            (pre, suf, summ)
        };

        out.push(InstanceFixture {
            instance_id,
            project,
            pass,
            run_dir: entry.path().to_path_buf(),
            prefix,
            suffix,
            summaries,
            recency: recency.max(now_unix_secs()),
        });
    }
    // Dedupe by instance_id, keep most-recent.
    let mut by_id: BTreeMap<String, InstanceFixture> = BTreeMap::new();
    for f in out {
        by_id
            .entry(f.instance_id.clone())
            .and_modify(|existing| {
                if f.recency > existing.recency {
                    *existing = f.clone();
                }
            })
            .or_insert(f);
    }
    Ok(by_id.into_values().collect())
}

/// "marshmallow-code__marshmallow-1359-7b-q4" → "marshmallow-1359"
/// "django__django-XXXX-7b-q4-a1" → "django-XXXX"
///
/// The short id is what `<short>-pathB.{prefix,suffix}` + `<short>.summaries.json`
/// use as their file stem in the fixture root.
fn short_instance_id(full: &str) -> String {
    // Drop the org__ prefix.
    let after_org = full.split_once("__").map(|(_, r)| r).unwrap_or(full);
    after_org.to_string()
}

fn strip_model_tags(s: &str) -> String {
    // Trailing tags like `-7b-q4`, `-7b-q4-autosynth`, `-1.5b-q8` are
    // appended by run_instance.py. We strip the LAST `-<model>-<rest>`
    // segment that starts with `-Nb` or similar; for robustness, fall back
    // to splitting at "-7b", "-1.5b", "-3b", "-14b", "-32b".
    for needle in ["-7b-", "-1.5b-", "-3b-", "-14b-", "-32b-"] {
        if let Some(idx) = s.find(needle) {
            return s[..idx].to_string();
        }
    }
    s.to_string()
}

fn count_f2p_passes(run_dir: &Path) -> Result<u32> {
    let path = run_dir.join("f2p.json");
    let text = std::fs::read_to_string(&path).with_context(|| format!("read {path:?}"))?;
    let v: serde_json::Value = serde_json::from_str(&text)?;
    Ok(v.as_array().map(|a| a.len() as u32).unwrap_or(0))
}

fn count_jsonl_rows(path: &Path) -> Result<usize> {
    let text = std::fs::read_to_string(path)?;
    Ok(text.lines().filter(|l| !l.trim().is_empty()).count())
}

/// Stream `<inst>.jsonl` into the aggregated file, injecting
/// `event.data.instance_id` per row.
fn aggregate(
    agg: &mut std::io::BufWriter<std::fs::File>,
    inst_jsonl: &Path,
    instance_id: &str,
) -> Result<()> {
    use std::io::Write;
    let text = std::fs::read_to_string(inst_jsonl)?;
    for line in text.lines() {
        if line.trim().is_empty() {
            continue;
        }
        // Parse → modify event.data.instance_id → serialize back.
        let mut v: serde_json::Value = serde_json::from_str(line)?;
        if let Some(data) = v
            .get_mut("event")
            .and_then(|e| e.get_mut("data"))
            .and_then(|d| d.as_object_mut())
        {
            data.insert(
                "instance_id".to_string(),
                serde_json::Value::String(instance_id.to_string()),
            );
        }
        let s = serde_json::to_string(&v)?;
        agg.write_all(s.as_bytes())?;
        agg.write_all(b"\n")?;
    }
    Ok(())
}

fn now_unix_secs() -> u64 {
    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .map(|d| d.as_secs())
        .unwrap_or(0)
}
